void main() {
  int day = 1;
  if (day == 1) {
    print('Sunday');
  } else if (day == 2) {
    print('Monday');
  } else if (day == 3) {
    print('Tuesday');
  } else if (day == 4) {
    print('Wednesday');
  } else if (day == 5) {
    print('Thursday');
  } else if (day == 6) {
    print('Friday');
  } else {
    print('Saturday');
  }
}
